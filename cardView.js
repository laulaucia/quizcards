angular.module('quizCards')
  .directive('card', cardView);

function cardView(){
  var directive = {};

  //'A' == attribute, 'E' == element, 'C' == class
  directive.restrict = 'E';
  directive.replace = true;
  directive.templateUrl =  "card-show.html";
  directive.scope = {
      id: '=',
      prompt: '@',
      answer: '@',
      deck: '&'
  };
  return directive;
}